//Herança, Polimorfismo, Encapsulamento e Abstração
/* Herança: Cada Objeto pode ter um Objeto pai de onde tiora suas caracteristicas
Polimorfismo: Tem caracteristicas do Objeto pai ou Avo, porém se comportam de forma diferente
Encapsulamento: possuem caracteristicas únicas diferentes do restante do código
Abstração: São as diferenças entre os Objetos, Ex: Um carro é um Veículo,
um Avião também é um veículo, porém ambos são bem diferentes.*/